$(document).on('ready',function(){
	$('.location').click(function(){
		$('.tags-search').css({"display":"none"});
		$('.location-search').css({"display":""});
	})
	$('.tags').click(function(){
		$('.tags-search').css({"display":"inline-block"});
		$('.location-search').css({"display":"none"});
	})
})
